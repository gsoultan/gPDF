// GetStandardWidth returns the advance width of a rune in a standard font (e.g. Helvetica).
// Returns 0 if not found.
func GetStandardWidth(fontName string, r rune) int {
	switch fontName {
	case "Helvetica", "Helvetica-Bold", "Helvetica-Oblique", "Helvetica-BoldOblique":
		return HelveticaWidths[r]
	case "Symbol":
		return SymbolWidths[r]
	case "ZapfDingbats":
		return ZapfDingbatsWidths[r]
	default:
		return 0
	}
}

